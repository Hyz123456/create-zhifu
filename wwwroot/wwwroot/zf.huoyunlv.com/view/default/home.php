                                <p>
                                    企业向渠道申请支付权限时需要提供相关资质进行认证。申请支付渠道的公司需为大陆注册公司，且五证齐全。五证分别为：营业执照、税务登记证、组织机构代码证、开户许可证、法人身份证。若为三证合一的公司，则只需提供营业执照、开户许可证、法人身份证。</p>
