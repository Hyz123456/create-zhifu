                                <p>中付宝
                                    平台只负责交易的处理，不参与资金结算，所有的资金都是由支付平台扣除相应的交易手续费后，按照固定的结算周期直接结算到商户的对应渠道账户。资金由支付渠道结算给商户，结算账户为支付渠道申请入网时提供的企业账户。</p>
